                            SO <- FindClusters(SO, resolution = clustering_params$cluster_resolution[which(clustering_params$celltype == ct)], graph.name = "SCT_snn")
                            SO <- RunUMAP(object = SO, dims = parse_dims(clustering_params$lsi_dims[which(clustering_params$celltype == ct)]), reduction = "harmony", reduction.name = "umap_harmony")
                            
                          }else{
                            
