# Calculate ratio of marker genes and annotate clusters ------------------------
compute_marker_ratios <- function(SO, assay = "SCT", slot = "counts", eps = 1e-4) {
  stopifnot(requireNamespace("Seurat", quietly = TRUE))
  
  # markers
  markers <- list(
    GABA    = "Gad2",
    VGlut1  = "Slc17a7",
    VGlut2  = "Slc17a6",
    non_Neuro = c("Slc1a3","Mal","Mog","Pdgfra","C1qb","Sostdc1")
  )
  
  # grab counts (genes x cells)
  mat <- Seurat::GetAssayData(SO, assay = assay, slot = slot)
  
  # helper: vector of counts per cell for a gene; 0 if missing
  get_gene <- function(g) {
    if (g %in% rownames(mat)) as.numeric(mat[g, ]) else rep(0, ncol(mat))
  }
  
  # individual genes
  gad2    <- get_gene(markers$GABA)
  slc17a7 <- get_gene(markers$VGlut1)
  slc17a6 <- get_gene(markers$VGlut2)
  
  # non-neuronal sum across its marker set (per cell)
  nn_genes_present <- intersect(markers$non_Neuro, rownames(mat))
  non_neuro_sum <- if (length(nn_genes_present) > 0) {
    colSums(mat[nn_genes_present, , drop = FALSE])
  } else {
    rep(0, ncol(mat))
  }
  
  # ratios (add small eps to avoid div-by-zero & log issues)
  df <- data.frame(
    row.names = colnames(mat),
    GABA_per_VGlut1      = (gad2    + eps) / (slc17a7 + eps),
    GABA_per_VGlut2      = (gad2    + eps) / (slc17a6 + eps),
    GABA_per_non_Neuro   = (gad2    + eps) / (non_neuro_sum + eps),
    VGlut1_per_non_Neuro = (slc17a7 + eps) / (non_neuro_sum + eps),
    VGlut1_per_GABA      = (slc17a7 + eps) / (gad2 + eps),
    VGlut2_per_non_Neuro = (slc17a6 + eps) / (non_neuro_sum + eps),
    VGlut2_per_GABA      = (slc17a6 + eps) / (gad2 + eps),
    non_Neuro_per_GABA   = (non_neuro_sum + eps) / (gad2    + eps),
    non_Neuro_per_VGlut1 = (non_neuro_sum + eps) / (slc17a7 + eps),
    non_Neuro_per_VGlut2 = (non_neuro_sum + eps) / (slc17a6 + eps)
  )
  
  df$Cluster <- SO@meta.data$seurat_clusters
  
  df_summary <- df %>% group_by(., Cluster) %>% summarise_all(funs(mean))
  
  df_summary[,2:ncol(df_summary)] <- df_summary[,2:ncol(df_summary)] %>% t() %>% scale() %>% t() %>% as_tibble()
  
  df_for_plt <- df_summary %>% reshape2::melt()
  
  
  return(df_for_plt)
}

