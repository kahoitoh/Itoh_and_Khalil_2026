# Re-perform peakcalling, perform dimension reductio
SO_singlet_list <- lapply(SO_singlet_list, 
                          function(SO){
                            DefaultAssay(SO) <- "ATAC"
                            
                            # peak call
                            peaks <- CallPeaks(SO, macs2.path = "/path/to/macs2")
                            
                            peaks <- keepStandardChromosomes(peaks, pruning.mode = "coarse")
                            peaks <- subsetByOverlaps(x = peaks, ranges = Signac::blacklist_mm10, invert = TRUE)
                            
                            # count 
                            macs2_counts <- FeatureMatrix(
                              fragments = Fragments(SO),
                              features = peaks,
                              cells = colnames(SO)
                            )
                            
                            SO[["peaks"]] <- CreateChromatinAssay(
                              counts = macs2_counts,
                              annotation = annotation,
                              fragments = Fragments(SO)
                            )
                            
                            DefaultAssay(SO) <- "peaks"
                            
                            # Normalization and linear dimensional reduction 
                            SO <- FindTopFeatures(SO, min.cutoff = 5)
                            SO <- RunTFIDF(SO)
                            SO <- RunSVD(SO)
                            
                            SO
                          })

