#!/usr/bin/env Rscript

# ==============================================================================
# scMultiome preprocessing, clustering, and annotation pipeline
# ------------------------------------------------------------------------------
# Purpose:
#   Build a Seurat object from 10x Multiome RNA/ATAC data, perform RNA/ATAC QC,
#   run hierarchical RNA-based clustering, remove putative doublets based on
#   mixed lineage marker expression, re-call peaks in singlet cells, calculate
#   chromVAR motif deviation scores for downstream motif analysis, and assign
#   final cell-type annotations using external cluster annotation tables.
#
# Workflow:
#   1. Load 10x Multiome gene expression and ATAC peak count matrices.
#   2. Create a Seurat object with RNA and ATAC assays.
#   3. Perform RNA/ATAC quality control using region-specific thresholds.
#   4. Run first-round clustering to assign broad cell classes.
#   5. Split cells into excitatory neurons, inhibitory neurons, and glia.
#   6. Run second-round clustering within each broad cell class.
#   7. Inspect lineage marker expression and remove manually annotated doublets,
#      undetermined clusters, and "only" clusters.
#   8. Reconstruct singlet-cell Seurat objects from the QC-filtered object.
#   9. Re-run RNA normalization, peak calling, ATAC dimensional reduction, and
#      chromVAR on singlet cells.
#  10. Run third-round clustering and assign final cell-type annotations.
#
# Notes for reproducibility:
#   - Edit only the CONFIG section before running on a new machine or dataset.
#   - REGION controls region-specific QC, clustering, and annotation parameters.
#   - Parameter tables are read from external metadata files to keep manual
#     thresholds and cluster annotations explicit.
#   - For mPFC, RNA assays are split by Sample and integrated with Harmony.
#   - Putative doublets are identified manually from second-round clusters using
#     mutually incompatible lineage marker expression.
#   - Final objects are saved as a list of Seurat objects:
#       Exc_Neu, Inh_Neu, and Glia.
#
# Required external files:
#   - filtered_feature_bc_matrix.h5
#   - filtered_feature_bc_matrix/features.tsv
#   - atac_fragments.tsv.gz
#   - SeuratQCvalues_histone_MO.txt
#   - ClusteringParamter_scMultiome.txt
#   - CellTypeAnnotation_whole_nuclei_scMultiome.txt
#   - JASPAR2024.sqlite3
#
# Major outputs:
#   - SO_SeuratQC.rds
#       QC-filtered multiome object before hierarchical clustering.
#   - second_clustering_doublet_marker_FeaturePlot_*.pdf
#       Marker gene FeaturePlots used to inspect putative doublet clusters.
#   - SO_list_Annotated.rds
#       Final annotated singlet-cell Seurat object list after third clustering.
#
# Important:
#   - This pipeline assumes that the sample suffix in cell barcodes encodes
#     sample identity as 1, 2, 3, or 4.
#   - Manual annotation tables must match the cluster numbers generated by the
#     corresponding first, second, and third clustering steps.
#   - Re-running the script after changing clustering parameters may require
#     updating the annotation table.
# ==============================================================================

